package kr.s04.operator;

public class OperatorMain02 {
	public static void main(String[] args) {
		System.out.println("===산술연산자==="); //2순위 연산자 
		System.out.println(10+10);
		System.out.println(20-7);
		System.out.println(10*3);
		System.out.println(10/3);//나눗셈 (몫 구하기)
		System.out.println(5%3);//나머지 구하기
		System.out.println("-------------");
		
		System.out.println(10.0/3); //하나는 double로 변경해야 함 (3.3333333333333335)
		
		
		
		
	}
}
