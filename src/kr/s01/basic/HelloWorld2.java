package kr.s01.basic;
public class HelloWorld2 { //HelloWorld -> 클래스 명
	public static void main(String[] args) {  // {}은 메인 함수를 의미, 프로그램 진입점(필수!)
		System.out.println("오늘은 화요일");  // ""은 함수를 의미, System.out.println->콘솔에 출력
		System.out.println("내일은 수요일");  // println 출력 후 줄바꿈
	}
}

